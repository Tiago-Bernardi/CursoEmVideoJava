    private void btnDividirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDividirActionPerformed
        // TODO add your handling code here:
        int num = Integer.parseInt(txtNum.getText());
        int den = Integer.parseInt(txtDen.getText());
        float div = num / den;
        float res = num % den;
        float raiznum = (float) Math.sqrt(num); //typecast
        double raizden = Math.sqrt(den);
        lblDiv.setText(Float.toString(div));
        lblRes.setText(Float.toString(res));
        lblRaizNum.setText(Float.toString(raiznum));
        lblRaizDen.setText(Double.toString(raizden));
        
    }//GEN-LAST:event_btnDividirActionPerformed

